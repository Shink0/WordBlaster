package com.wordblaster.renderer;

public class DisplaySettings {

    private int resolution_h;
    private int resolution_l;

    public DisplaySettings() {
    }

    public int getResolution_h() {
        return resolution_h;
    }

    public void setResolution_h(int resolution_h) {
        this.resolution_h = resolution_h;
    }

    public int getResolution_l() {
        return resolution_l;
    }

    public void setResolution_l(int resolution_l) {
        this.resolution_l = resolution_l;
    }

}
